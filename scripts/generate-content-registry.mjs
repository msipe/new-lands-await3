import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";

const repoRoot = process.cwd();
const registryDir = resolve(repoRoot, "content", "registry");
const outputFile = resolve(repoRoot, "src", "planning", "content-registry-generated.ts");

function loadJson(fileName) {
  const source = readFileSync(resolve(registryDir, fileName), "utf8");
  return JSON.parse(source);
}

const npcs = loadJson("npcs.json");
const tiles = loadJson("tiles.json");
const quests = loadJson("quests.json");
const bigBads = loadJson("big-bads.json");
const enemies = loadJson("enemies.json");

const generated = `/* eslint-disable */\n// Auto-generated by scripts/generate-content-registry.mjs.\n// Source of truth: content/registry/*.json\n\nexport const RAW_NPCS = ${JSON.stringify(npcs, null, 2)} as const;\n\nexport const RAW_TILES = ${JSON.stringify(tiles, null, 2)} as const;\n\nexport const RAW_QUESTS = ${JSON.stringify(quests, null, 2)} as const;\n\nexport const RAW_BIG_BADS = ${JSON.stringify(bigBads, null, 2)} as const;\n\nexport const RAW_ENEMIES = ${JSON.stringify(enemies, null, 2)} as const;\n`;

mkdirSync(dirname(outputFile), { recursive: true });
writeFileSync(outputFile, generated, "utf8");
